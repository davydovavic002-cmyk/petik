export function t(
  language: Language,
  key: TranslationKey,
  params?: Record<string, string | number>,
): string {
  let text: string = translations[language][key];

  if (params) {
    Object.entries(params).forEach(([param, value]) => {
      text = text.replace(`{${param}}`, String(value));
    });
  }

  return text;
}

